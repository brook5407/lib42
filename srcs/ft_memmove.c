#include "libft.h"

void	*ft_memmove(void *dst, const void *src, size_t len)
{
	unsigned char	*dest;
	unsigned char	*srcs;

	dest = (unsigned char *)dst;
	srcs = (unsigned char *)src;
	if (dest > srcs)
		while (len--)
			*(dest + len) = *(srcs + len);
	else
		ft_memcpy(dst, src, len);
	return (dst);
}
