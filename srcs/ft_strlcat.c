#include "libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t dstsize)
{
	size_t	size;
	size_t	srcsize;

	size = dstsize;
	while (*dst)
	{
		dst++;
		if (size > 0)
			size--;
	}
	srcsize = ft_strlcpy(dst, src, size);
	return (srcsize + dstsize - size);
}
