#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*s2;
	size_t	len;

	len = ft_strlen(s1) + 1;
	s2 = malloc(sizeof(char) * len);
	if (s2 == NULL)
		return (0);
	ft_memcpy(s2, s1, len);
	return (s2);
}
